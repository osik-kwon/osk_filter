	uint16_t _unicode_to_KSSM(uint16_t uc)
	{
		uint16_t kssm, u;
		uint16_t cho, jung, jong;
		u = uc;
		kssm = 0;
		if ((u >= 0x0000) && (u < 0x0080)) {
			kssm = u;
		}
		else if ((u >= 0x1100) && (u <= 0x11f9)) {
			kssm = uk2j_sub(u, 0x00);
		}
		else if ((u >= 0x3131) && (u <= 0x318e)) {
			kssm = uk2j_sub(u, 0x00);
		}
		else if ((u >= 0xac00) && (u <= 0xd7a3)) {
			// unicode 한글영역 
			u -= 0xac00;
			jong = u % 28;
			jung = (u % (28 * 21)) / 28;
			cho = u / (28 * 21);
			// make kssm code
			cho = _u2j_cho[cho];
			jung = _u2j_jung[jung];
			jong = _u2j_jong[jong];
			kssm = 0x8000 | (cho << 10) | (jung << 5) | (jong);
		}
		else if ((u >= 0xffa1) && (u <= 0xffdc)) {
			// 한글 자모음 
			u -= 0xffa1;
			kssm = _ffa1_ffdc[u];
		}
		else {
			kssm = 0;
		}
		return(kssm);
	}

	uint16_t _KSSM_to_unicode(uint16_t kc)
	{
		int esti;
		uint16_t kssm, u;
		uint16_t cho, jung, jong;
		kssm = kc;
		u = 0;
		esti = 0;
		if ((kssm >= 0x0000) && (kssm < 0x0080)) {
			u = kssm;
		}
		else {
			if (kssm & 0x8000) {
				// direct extract jamo
				cho = jung = jong = 0;
				cho = (kssm & 0x7c00) >> 10;
				jung = (kssm & 0x03e0) >> 5;
				jong = kssm & 0x001f;
				if ((cho < 1) || (jung < 2) || (jong < 1))    return(0); // wrong kssm
				if ((cho > 20) || (jung > 29) || (jong > 29)) return(0); // wrong kssm
				if ((cho == 1) && (jung == 2) && (jong == 1)) return(0); // wrong kssm ( 모두다 채움문자 )
				esti = 0;
				if ((cho > 1) && (jung == 2) && (jong == 1)) esti = 1; // 초성 낱자음 
				if ((cho == 1) && (jung > 2) && (jong == 1)) esti = 2; // 중성 낱자음
				if ((cho == 1) && (jung == 2) && (jong > 1)) esti = 3; // 종성 낱자음
				if (esti) {
					// 한글 자모음 일 경우 
					u = j2uk_sub(kssm, 0x00);
				}
				else {
					// unicode 용 초중종성 번호로 바꾼다
					cho = _j2u_cho[cho];
					jung = _j2u_jung[jung];
					jong = _j2u_jong[jong];
					//				(유니코드 완성형 한글코드) = 0xAC00 + 28*21*(초성인덱스) + 28*(중성인덱스) + (종성인덱스)
					u = 0xac00 + (28 * 21 * cho) + (28 * jung) + jong;
				}
			}
		}
		return(u);
	}

	int get_oem_code_page(uint16_t code)
	{
		int i, j, k;
		if ((code >= 0x8141) && (code <= 0xc8fe)) {

			i = j = k = 0;

			// 좀 더 탐색 시간을 단축하기 위한

			k = (int)((code & 0x7f00) >> 8); // 1~ 72;

			if ((k >= 1) && (k <= 36))        j = (k - 1) * 3;
			else if ((k >= 37) && (k <= 48))  j = 109 + (k - 37) * 3;
			else if ((k >= 49) && (k <= 70))  j = 146 + (k - 49) * 4;
			else                       j = 232;

			for (i = j; i < _CP949_PAGE; i++)
			{
				if ((code >= cp949[i].first) && (code <= cp949[i].last))return(i);
			}
		}
		return(-1);
	}


	uint16_t _KS_to_unicode(uint16_t ksc)
	{
		uint16_t i, j, k;
		int page;
		uint16_t u, oem, offset, cnt;
		uint8_t* p;

		u = oem = offset = cnt = 0;
		i = j = k = 0;
		page = get_oem_code_page(ksc);
		if (page >= 0)
		{
			oem = cp949[page].first;
			u = cp949[page].uc_first;
			offset = cp949[page].offset;
			cnt = cp949[page].cnt;
			p = (uint8_t*)& _OEM_DATA[offset];
			for (i = 0; i < cnt; i++, oem++)
			{
				if (oem < ksc) u += (*p++);
				else {
					if (i) u += (*p++);
					break;
				}
			}
		}
		return(u);
	}

