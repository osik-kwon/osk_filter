	uint16_t _get_compo(int i, uint8_t cp)
	{
		uint16_t n;
		n = 0;
		if ((i >= 0) && (i < _CP949_PAGE))
		{
			switch (cp)
			{
			case 0x00: n = cp949[i].first; break;
			case 0x01: n = cp949[i].last; break;
			case 0x02: n = cp949[i].uc_first; break;
			case 0x03: n = cp949[i].uc_last; break;
			case 0x04: n = cp949[i].offset; break;
			case 0x05: n = cp949[i].cnt; break;
			}
		}
		return(n);
	}

